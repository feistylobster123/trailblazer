export const raceSummaries: RaceSummary[] = races.map((race) => ({
  id: race.id,
  slug: race.slug,
  name: race.name,
  distance: race.distance,
  distanceMi: race.distanceMi,
  difficulty: race.difficulty,
  location: race.location,
  imageUrl: race.imageUrl,
  nextEditionDate: race.currentEdition?.startDate,
  status: race.currentEdition?.status ?? 'upcoming',
  featured: race.featured,
}))

export function getRaceById(id: string): Race | undefined {
  return races.find((r) => r.id === id || r.slug === id)
}

export function getRaceSummaryById(id: string): RaceSummary | undefined {
  return raceSummaries.find((r) => r.id === id || r.slug === id)
}
